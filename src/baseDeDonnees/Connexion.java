    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        
        if (!txtUser.getText().equals("") && !txtPwd.getText().equals("")) {
            rs = db.querySelectAllWithParam("employes", "matricule='" + txtUser.getText() + "' and password='" + txtPwd.getText() + "'");
        
            try {
                while (rs.next()) {
                    username = rs.getString("matricule");
                    password = rs.getString("password");
                    poste = rs.getString("id_poste");
                }
                SessionLogin login =  new SessionLogin(username, password);
            } catch (SQLException ex) {
                //Logger.getLogger(Connexion.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (username == null || password == null) {
                JOptionPane.showMessageDialog(this, "Le nom d'utilisateur ou le mot de passe est incorrect");
            } else {
                if (poste.equals("1")) {
                    Accueil_Admin admin = new Accueil_Admin();
                    admin.setVisible(true);
                    this.dispose();
                } else if (poste.equals("2")) {
                    Accueil_Caissiere caissier = new Accueil_Caissiere();
                    caissier.setVisible(true);
                    this.dispose();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs.");
        }
        
    }//GEN-LAST:event_btnConnectActionPerformed

